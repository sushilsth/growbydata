                        <p>
                            In accordance with the technological change and demands in the society, the Nepali government made computer classes mandatory - practical and theoretical -  for students of grades 6-8.  However, lack of properly trained teachers and computer equipment, and very difficult coursework have caused difficulty in implementing these programs.
                        </p>
                        <p>As a data technology company, we at GrowByData believe knowledge of computers is critical for every individual. For some, it will give practical skills for their professional lives. For others, it will empower them in ways that are hard to anticipate. Accordingly, we formed a committee at GrowByData to design a computer training course to enhance the computer skills of underprivileged students attending a government public school. The course’s purpose was to enhance the practical knowledge of computers for underprivileged students and trigger their interest in technology. One of our major goals was to run effective classes with limited available resources and engage students in every aspect of the class. Ultimately, we wanted to encourage them to consider computer as a problem-solving tool rather than a tough device to learn. </p>
                        <p>As a pilot project, we conducted the program for students of grades 7 and 8 of Shree Mangala Devi Madhyamik Vidyalaya. We held 20 hours program over 8 months. GrowByData team members shared their expertise and invested time to train students and teachers. We focused on basic and practicable courses like making presentations and videos, keeping financial records in excel and more. We also conducted certification exams.  However, unlike a traditional exam, we did not grade them. Rather their performance levels were evaluated, and visible problems corrected. Our course covered the fundamentals of MS Word, MS PowerPoint, MS Excel, Nepali Typing, Web Page Design, Data Transfer Best Practices and Basic Email. </p>

                        <p>
                            The project ran smoothly with participation from almost every GrowByData department– Director, Administration, Software Development Teams, Analyst Teams, etc. In the theory classes, we used PowerPoint presentations and slideshows for effective visual teaching. We conducted classes in simple words - in Nepali and English - for full comprehension. The students reacted positively, and showed interest and excitement, throughout the course. Both the students and teachers were very responsive to the practical classes, which were a key part of the weekly lessons.
                        </p>
                        <p>
                            They really wanted to learn the know-hows of computers but weren’t getting any trainings or instruction to satisfy their curiosity. One major problem was the contents of the government coursework, which could not be taught by the minimally trained teachers. So, a GrowByData team member volunteered to teach the students a basic course on Web Page Design. This was taught on a special request by the students as it was one of the toughest chapters for them. Also, we trained the teachers along the students. We also checked the condition of the computer labs. Our team fixed broken machines and other issues in consultation from our computer vendors. At the end of the course, we conducted a final examination and awarded certification of completion of training to all participants. The trainers were also provided with an appreciation letter for their volunteer work.
                        </p>
                        <p>
                            To enable the students to use computers, we presented new two computer sets to the school, purchased from funds collected by our GrowByData team members and a matching grant from GrowByData.  We wanted to encourage the students and teachers to practice on the computers, and not take computers as a difficult to learn subject.
                        </p>
                        <p>
                            The GrowByData team wants to thank the principal, staff and students of Shree Mangala Devi Madhyamik Vidyalaya for being excellent hosts and making us feel very welcomed on every visit. We hope the students continue to learn computers and utilize the power of computers in their lives.
